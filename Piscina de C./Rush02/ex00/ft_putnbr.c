#include <unistd.h>

void	ft_putnbr(int nb)
{
	int	i;
	int	arr[10];

	i = 0;
	if (nb == 0)
		write(1, "0", 1);
	if (nb == -2147483648)
		write(1, "-2147483648", 11);
	else if (nb < 0 && nb != -2147483648)
	{
		write(1, "-", 1);
		nb *= -1;
	}
	while (nb > 0)
	{
		arr[i++] = nb % 10;
		nb /= 10;
	}
	while (i--)
	{
		arr[i] = arr[i] + '0';
		write(1, &arr[i], 1);
	}
}
